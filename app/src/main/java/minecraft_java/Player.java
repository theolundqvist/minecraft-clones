package minecraft_java;

public class Player {
    private float x;
    private float y;
    private float z;


    public Player() {
        this.x = 0f;
        this.y = 1f;
        this.z = 0f;
    }


    public Player(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }


}
